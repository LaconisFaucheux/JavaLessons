public class RelationalFun {
    public static void main(String[] args) {
        String name1 = "Nicolas";
        String name2 = "Clovis";

        boolean comparison = name1.equals(name2);
        boolean otherComparison = name1 == name2;

        System.out.println(comparison);
        System.out.println(otherComparison);
    }
}
